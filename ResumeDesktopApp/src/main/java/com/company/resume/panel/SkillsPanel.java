    private void reloadTable(JFrame frame) {
        Timer timer = new Timer(1000, null);
        timer.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                fillTable();

                if (!frame.isShowing()) {
                    timer.stop();
                }
            }
        });
        timer.start();
    }

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int[] selectedRows = tblSkills.getSelectedRows();

        if (selectedRows.length == 0) {
            return;
        }

        int confirmAnswer = showDeleteConfirmDialog();

        if (confirmAnswer == 0) {
            deleteSelectedItems(selectedRows);
            fillTable();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if (tblSkills.getSelectedRow() < 0) {
            return;
        }
        
        frameSkillUpdate = new SkillUpdate(userSkills.get(tblSkills.getSelectedRow()));
        frameSkillUpdate.setVisible(true);
        reloadTable(frameSkillUpdate);

    }//GEN-LAST:event_btnEditActionPerformed

