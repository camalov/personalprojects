    private int[] getSelectedItemsUserSkillId(int[] selectedRows) {
        int[] selectedUserSkillsId = new int[selectedRows.length];

        for (int i = 0; i < selectedRows.length; i++) {
            selectedUserSkillsId[i] = userSkills.get(selectedRows[i]).getId();
        }

        return selectedUserSkillsId;
    }

    private boolean deleteSelectedItems(int[] selectedRows) {
        return userSkillDao.removeSelectedItems(getSelectedItemsUserSkillId(selectedRows));
    }

    private int showDeleteConfirmDialog() {
        return JOptionPane.showOptionDialog(null,
                "Are you sure you want to delete ?",
                "Confirm Window",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                new String[]{"Yes, please",
                    "Cancel"},
                "");
    }


