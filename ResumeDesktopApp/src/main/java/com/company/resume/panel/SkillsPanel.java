    private void fillTable() {
        User user = Config.loggedInUser;
        userSkills = userSkillDao.getAllSkillByUserId(user.getId());

        Vector<Vector> raws = new Vector<>();
        userSkills.forEach(skill -> {
            Vector raw = new Vector();
            raw.add(skill.getSkill().getName());
            raw.add(skill.getPower());
            raws.add(raw);
        });
        Vector<String> columns = new Vector<>();
        columns.add("Skill");
        columns.add("Power");

        DefaultTableModel model = new DefaultTableModel();
        tblSkills.setModel(model);
        model.setDataVector(raws, columns);
    }

    public void fillComponents() {
        fillTable();
    }

